    private void BenefitFromOffer() {
        try{
            String nb_of_points = null, loyalty_points = null;
            int row = loyprog_table.getSelectedRow();
            String offer_id = loyprog_table.getModel().getValueAt(row, 0).toString();
            String query = "SELECT nb_points " +
                            "FROM loy_prog_offers " + 
                            "WHERE offer_id = " + offer_id;
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                nb_of_points = rs.getString(1);
            }  
            int pointsneeded = Integer.parseInt(nb_of_points);

            query = "SELECT loyalty_points " +
                            "FROM visitor " + 
                            "WHERE visitor_username = '" + username + "'";
            rs = stmt.executeQuery(query);
            while (rs.next()) {
                loyalty_points = rs.getString(1);
            }
            int mypoints = Integer.parseInt(loyalty_points);
            
            if (pointsneeded > mypoints) {
                JOptionPane optionPane = new JOptionPane("You do not have enough loyalty points to benefit from this offer.");
                JDialog dialog = optionPane.createDialog("WARNING!");
                dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                dialog.setVisible(true);
                return;
            }
            
            query = "SELECT visitor_username " +
                        "FROM benefit_from_offers " + 
                        "WHERE visitor_username = '" + username + "' AND offer_id = " + offer_id;
            rs = stmt.executeQuery(query);
            int count2=0;
            String hasrated=null;
            while (rs.next()) {
                hasrated = rs.getString(1);
                System.out.println(hasrated);
                if (hasrated.equals(username))
                {
                    count2++;
                }
            }

            if (count2==1) {
                JOptionPane optionPane = new JOptionPane("You cannot benefit from an offer twice.");
                JDialog dialog = optionPane.createDialog("WARNING!");
                dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                dialog.setVisible(true);
                return;
            }
            else {
                JOptionPane optionPane = new JOptionPane("You have successfuly benefited from this offer!");
                JDialog dialog = optionPane.createDialog("SUCCESS!");
                dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                dialog.setVisible(true);
            }
            String update = "UPDATE visitor " +
                    "SET loyalty_points = loyalty_points - " + pointsneeded + " " +
                    "WHERE visitor_username = '" + username + "' AND loyalty_points >= " + pointsneeded;
            int temp = stmt.executeUpdate(update);
            
            String insert = "INSERT INTO benefit_from_offers VALUES " +
                            "('" + username + "', " + offer_id + ")";
            temp = stmt.executeUpdate(insert);
            ListLoyProgOffers();
        }
        catch (SQLException e){
            System.out.println("SQL Exception: "+ e.toString());
        }
    }

