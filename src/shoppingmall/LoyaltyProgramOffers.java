    private void ListLoyProgOffers() {
        try{
            DefaultTableModel model = (DefaultTableModel) loyprog_table.getModel();
            model.setRowCount(0);
            String query = "SELECT offer_id, nb_points " +
                            "FROM loy_prog_offers " + 
                            "ORDER BY nb_points ASC";
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                String offer_id = rs.getString(1);
                String nb_points = rs.getString(2);
                model = (DefaultTableModel) loyprog_table.getModel();
                model.addRow(new Object[]{offer_id, nb_points});
                loyprog_table.setModel(model);
            }
            query = "SELECT loyalty_points " +
                            "FROM visitor " + 
                            "WHERE visitor_username = '" + username + "'";
            rs = stmt.executeQuery(query); 
            while (rs.next()) {
                String pts = rs.getString(1);
                points.setText(pts);
            } 
        }
        catch (SQLException e){
            System.out.println("SQL Exception: "+ e.toString());
        }
    }
    
